# 2. SSH HARDENING & PUBKEY INJECTION #
# =================================== #

setup_ssh() {
  local home_dir
  home_dir=$(eval "echo ~${NEW_USER}")
  local ssh_dir="${home_dir}/.ssh"
  local keys_file="${ssh_dir}/authorized_keys"
  mkdir -p "${ssh_dir}"
  if [[ -f "${keys_file}" ]]; then
    if ! grep -qF "${TERMIUS_PUBKEY}" "${keys_file}"; then
      echo "${TERMIUS_PUBKEY}" >>"${keys_file}"
      info "Public key injected into ${NEW_USER}'s authorized_keys"
    fi
  else
    echo "${TERMIUS_PUBKEY}" >>"${keys_file}"
    info "Public key injected into ${NEW_USER}'s authorized_keys"
  fi
  chown -R "${NEW_USER}:${NEW_USER}" "${ssh_dir}"
  chown -R "${NEW_USER}:${NEW_USER}" "${keys_file}"
  chmod 700 "${ssh_dir}"
  chmod 600 "${keys_file}"

  local sshd_conf_custom="/etc/ssh/sshd_config.d/${NEW_USER}.conf"
  if [[ ! -s "${sshd_conf_custom}" ]]; then
    tee "${sshd_conf_custom}" <<EOF >/dev/null
PasswordAuthentication no
KbdInteractiveAuthentication no
PubkeyAuthentication yes
PermitRootLogin no
ClientAliveInterval 60
UsePAM yes
EOF
    if sshd -t; then
      if systemctl is-active --quiet ssh.socket; then
        if systemctl daemon-reload && systemctl restart ssh.socket; then
          info "SSH hardening applied successfully (socket)"
        fi
      else
        systemctl restart ssh && info "SSH hardening applied successfully"
      fi
    else
      rm "${sshd_conf_custom}"
      error "SSHD configuration syntax error, rolled back changes"
    fi
  else
    warn "SSH custom configuration already exists, skipping."
  fi
}

