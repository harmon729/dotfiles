# ============================= #
# 3. KERNEL TUNING (BBR & SWAP) #
# ============================= #

setup_sysctl() {
  local need_reload=false
  local conf_file="/etc/sysctl.conf"
  local net_qdisc="${NET_QDISC}"
  local tcp_cong="${TCP_CONG}"
  local swappiness="${SWAPPINESS}"
  declare -A params=(
    ["net.core.default_qdisc"]="${net_qdisc}"
    ["net.ipv4.tcp_congestion_control"]="${tcp_cong}"
    ["vm.swappiness"]="${swappiness}"
  )
  for key in "${!params[@]}"; do
    if [[ $(sysctl -n "${key}") != "${params["${key}"]}" ]]; then
      sed -i "/^[[:space:]]*${key}/d" "${conf_file}"
      echo "${key} = ${params["${key}"]}" >>"${conf_file}"
      need_reload=true
    fi
  done
  if [[ ${need_reload} == true ]]; then
    sysctl -p >/dev/null
    info "Kernel parameters optimized (BBR & Swappiness)"
  fi
}

