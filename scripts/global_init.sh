# ================================== #
# 1. USER & PRIVILEGE INITIALIZATION #
# ================================== #

setup_user() {
  if id "${NEW_USER}" &>/dev/null; then
    info "user ${NEW_USER} already exists, skipping creation."
  else
    useradd -m -s /bin/bash "${NEW_USER}" || error "failed to create user ${NEW_USER}"
    info "successfully created user ${NEW_USER}"
  fi

  for g in "${REQUIRED_GROUPS[@]}"; do
    if getent group "$g" &>/dev/null; then
      if ! id -nG "${NEW_USER}" | grep -qw "$g"; then
        usermod -aG "$g" "${NEW_USER}"
        info "add user ${NEW_USER} to group $g"
      fi
    fi
  done

  local sudo_conf="/etc/sudoers.d/${NEW_USER}"
  if [[ ! -s "${sudo_conf}" ]]; then
    echo "${NEW_USER} ALL=(ALL : ALL) NOPASSWD: ALL" | tee "${sudo_conf}" >/dev/null
    chmod 440 "${sudo_conf}"
    visudo -cf "${sudo_conf}" >/dev/null || error "sudoers syntax check failed"
    info "sudo passwordless access configured for ${NEW_USER}"
  else
    warn "${sudo_conf} already exists."
  fi
}
