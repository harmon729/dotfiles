setup_swap() {
  local swap_file="${SWAP_PATH}"
  if swapon --show=NAME --noheadings | grep -qxF "${swap_file}"; then
    warn "Swap is already active, skipping."
  else
    local free_kb
    free_kb=$(df --output=avail / | tail -n1)
    local swap_size="${SWAP_SIZE}"
    if [[ "${free_kb}" -lt "$((${swap_size%G} * 1024 * 1024 + 5 * 1024 * 1024))" ]]; then
      warn "Insufficient disk space, skipping swap creation."
      return 1
    fi
    if [[ -f "${swap_file}" ]]; then
      warn "${swap_file} already exists, skip."
    else
      info "Creating ${SWAP_SIZE} swap file. This may take a few seconds..."
      fallocate -l "${swap_size}" "${swap_file}"
      chmod 600 "${swap_file}"
      mkswap "${swap_file}" >/dev/null
      if ! swapon "${swap_file}"; then
        warn "fallocate failed to create a valid swap. Retrying with dd..."
        rm -rf "${swap_file}"
        dd if=/dev/zero of="${swap_file}" bs="1G" count="${swap_size%G}"
        chmod 600 "${swap_file}"
        mkswap "${swap_file}"
        swapon "${swap_file}" || {
          warn "dd failed to create a valid swap."
          return 1
        }
      fi
      if ! grep -q "${swap_file}" "/etc/fstab"; then
        echo "${swap_file} none swap sw 0 0" >>"/etc/fstab"
        info "Swap file created and activated successfully."
      fi
    fi
  fi
}

